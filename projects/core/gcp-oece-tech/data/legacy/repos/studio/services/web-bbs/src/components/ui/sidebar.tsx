const sidebarMenuButtonVariants = cva(
  "flex w-full items-center gap-2 overflow-hidden rounded-md p-2 text-left text-sm outline-none ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default: "hover:bg-accent hover:text-accent-foreground",
        ghost: "hover:bg-accent hover:text-accent-foreground",
      },
      isActive: {
        true: "bg-accent text-accent-foreground font-medium",
        false: "",
      },
    },
    defaultVariants: {
      variant: "ghost",
      isActive: false,
    },
  }
)

const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant = "ghost",
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state } = useSidebar()

    const buttonContent = (
      <div className="flex items-center gap-2">
        {React.Children.toArray(children).find(child => React.isValidElement(child) && (child.type as any).displayName?.includes('Icon'))}
        <span className={cn("truncate transition-opacity duration-200", state === 'collapsed' ? 'opacity-0' : 'opacity-100')}>
            {React.Children.toArray(children).find(child => typeof child === 'string' || (React.isValidElement(child) && !(child.type as any).displayName?.includes('Icon')))}
        </span>
      </div>
    );
    
    const button = (
      <Comp
        ref={ref}
        className={cn(sidebarMenuButtonVariants({ variant, isActive }), state === 'collapsed' ? 'justify-center !p-2' : '', className)}
        {...props}
      >
        {React.Children.map(children, (child) => {
            if (React.isValidElement(child) && child.type === 'span') {
                return React.cloneElement(child as React.ReactElement, {
                    className: cn("truncate transition-opacity duration-200", state === 'collapsed' ? 'opacity-0' : 'opacity-100')
                });
            }
             if (React.isValidElement(child)) {
                return React.cloneElement(child as React.ReactElement, {
                    className: cn("h-4 w-4 shrink-0", (child.props as any).className)
                });
            }
            return child;
        })}
      </Comp>
    )

    if (!tooltip) {
      return button
    }
    
    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={state !== "collapsed" || isMobile}
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

